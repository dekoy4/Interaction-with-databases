-- 07_denorm_table.sql
-- Денормализация в таблицу: добавление избыточного поля orders.customer_name

-- 1. Добавляем избыточное поле
ALTER TABLE orders ADD COLUMN customer_name TEXT;

-- 2. Заполняем его на момент текущих заказов
UPDATE orders o
SET customer_name = c.name
FROM customers c
WHERE c.customer_id = o.customer_id;

-- 3. Создаём индекс для ускорения поиска по имени
CREATE INDEX IF NOT EXISTS idx_orders_customer_name ON orders(customer_name);

-- 4. Обоснование (можно оставить в комментариях / README)
--
-- Зачем:
--  - ускорить поиск заказов по имени клиента без JOIN к customers;
--  - хранить "имя клиента на момент заказа" (исторический снимок).
--
-- Какая цена:
--  - при изменении customers.name заказы не обновляются;
--  - нужно либо синхронизировать через триггер/ETL, либо смириться с избыточностью.
--
-- Когда оправдано:
--  - когда часты запросы по имени клиента;
--  - когда обновления customers.name редки.
--
-- Когда не оправдано:
--  - если имя клиента изменяется часто и нужно его всегда актуальное состояние.